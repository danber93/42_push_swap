#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t		i;
	size_t		c;

	if (!src || !dst)
		return (0);
	c = ft_strlen(src);
	if (!dstsize)
		return (c);
	i = 0;
	while (src[i] != 0 && i < dstsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (c);
}
